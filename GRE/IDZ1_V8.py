# Задача:
# В группе 12 студентов, среди которых 8 отличников. Наудачу отобраны 9 студентов.
# Найти вероятность того, что среди отобранных студентов 5 отличников.

from math import comb

# Параметры задачи
N = 12  # общее количество студентов
M = 8   # количество отличников
n = 9   # количество выбранных студентов
k = 5   # количество выбранных отличников

# Вычисляем вероятность по формуле гипергеометрического распределения
P_X_equals_k = comb(M, k) * comb(N - M, n - k) / comb(N, n)

# Вывод результата
print(f"Вероятность того, что среди отобранных студентов 5 отличников: {P_X_equals_k:.4f}")

