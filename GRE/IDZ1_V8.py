# Задача:
# Вероятность перевыполнения обязательств первым заводом равна 0,9; вторым - 0,95. Какова вероятность того, что
# хотя бы один из заводов перевыполнит свои обязательства, если они реализуют свою продукцию независимо один от другого.

# Вероятности перевыполнения обязательств для каждого завода
P1 = 0.9
P2 = 0.95

# Вероятности НЕ перевыполнения обязательств для каждого завода
Q1 = 1 - P1  # Q1 = 1 - 0.9 = 0.1
Q2 = 1 - P2  # Q2 = 1 - 0.95 = 0.05

# Вероятность того, что ни один завод не перевыполнит свои обязательства
P_none = Q1 * Q2  # P_none = 0.1 * 0.05 = 0.005

# Вероятность того, что хотя бы один завод перевыполнит свои обязательства
P_at_least_one = 1 - P_none  # P_at_least_one = 1 - 0.005 = 0.995

print(f"Вероятность того, что хотя бы один завод перевыполнит свои обязательства: {P_at_least_one:.4f}")

