# Задача:
# Страховая компания выплачивает страховую сумму в среднем по 15% договоров.
# Найти вероятность того, что из десяти договоров с наступлением страхового случая будет
# связано с выплатой страховой суммы: а) три договора; б) менее двух договоров.

from math import comb

# Параметры задачи
n = 10  # договоры
p = 0.15  # вероятность выплаты по одному договору 15%
q = 1 - p  # вероятность отсутствия выплаты

# Функция для вычисления вероятности P(X = k)
def binomial(n, k, p):
    return comb(n, k) * (p**k) * ((1-p)**(n-k))

# Задача а): Вероятность того, что будет ровно три выплаты
k_a = 3
P_X_equals_3 = binomial(n, k_a, p)

# Задача б): Вероятность того, что будет менее двух выплат
P_X_less_than_2 = sum(binomial(n, k, p) for k in range(2))  # Сумма для k = 0 и k = 1

# Вывод результатов
print(f"Вероятность того, что выплаты будут связаны с тремя договорами: {P_X_equals_3:.4f}")
print(f"Вероятность того, что выплаты будут связаны с менее двумя договорами: {P_X_less_than_2:.4f}")
